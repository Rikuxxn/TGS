#include "CharacterManager.h"
#include "Character.h"


//=============================================================================
// キャラクター追加処理
//=============================================================================
void CCharacterManager::AddCharacter(CCharacter* pChar)
{
    // リストに追加する
    m_characters.push_back(pChar);
}
//=============================================================================
// 初期化処理
//=============================================================================
void CCharacterManager::Init(void)
{
    for (auto chara : m_characters)
    {
        // キャラクターの初期化処理
        chara->Init();
    }
}
//=============================================================================
// クリア処理
//=============================================================================
void CCharacterManager::Destroy(void)
{
    // リストのクリア(空にする)
    m_characters.clear();
}
//=============================================================================
// 更新処理
//=============================================================================
void CCharacterManager::Update(void)
{
    for (auto chara : m_characters)
    {
        // キャラクターの更新処理
        chara->Update();
    }
}
//=============================================================================
// 描画処理
//=============================================================================
void CCharacterManager::Draw(void)
{
    for (auto chara : m_characters)
    {
        // キャラクターの描画処理
        chara->Draw();
    }
}
